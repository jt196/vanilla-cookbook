<h1>Classic Chocolate Chip Cookies</h1>

<h2>Source: James Torr</h2>

<b>Ingredients:</b>
<ul>
	<li>1 cup unsalted butter, softened</li>
	<li>1 cup white sugar</li>
	<li>1 cup packed brown sugar</li>
	<li>2 large eggs</li>
	<li>1 teaspoon vanilla extract</li>
	<li>3 cups all-purpose flour</li>
	<li>1 teaspoon baking soda</li>
	<li>2 teaspoons hot water</li>
	<li>1/2 teaspoon salt</li>
	<li>2 cups semisweet chocolate chips</li>
</ul>

<p>
	<b>Directions:</b> Delicious classic chocolate chip cookies that are perfect for any occasion.
</p>

<p>
	<b>Instructions:</b> Preheat oven to 350 degrees F (175 degrees C). Cream together the butter, white
	sugar, and brown sugar until smooth. Beat in the eggs one at a time, then stir in the vanilla. Dissolve
	baking soda in hot water. Add to batter along with salt. Stir in flour and chocolate chips. Drop by
	large spoonfuls onto ungreased pans. Bake for about 10 minutes in the preheated oven, or until edges
	are nicely browned. Time: 30 minutes
</p>
